//Comparison operators:
var n1 = 5;
console.log(n1 == 5); //         Equals true
console.log(n1 === 5); //        type checking and value checking result true
console.log(n1 != 5); //         false
console.log(n1 > 8); //          false  
console.log(n1 < 8); //          true
console.log(n1 >= 8); //          false   
console.log(n1 <= 8); //          true
//Logical operators:    &&=AND ||=OR
//AND = All conditions true
var n = 11;
console.log(n >= 5 && n < 10); //
console.log(n >= 5 || n < 10); //  any condition is true then result will be true
console.log(n > 5 && n < 10); //
console.log(n >= 5 || n < 10); //
console.log(n > 5 || n < 10); //
